		create or replace function  EV_ADMIN.ComprobarUsuario(
		p_usuario EV_ADMIN.USUARIO.USUARIO%TYPE ,p_pass EV_ADMIN.USUARIO.PASS%TYPE)
		return EV_ADMIN.USUARIO.USUARIO%TYPE
		as
		  cursor c_comprobarUsuario is
			select USUARIO, PASS
			from EV_ADMIN.USUARIO;
			
		  v_usuario  EV_ADMIN.USUARIO.USUARIO%TYPE;
		  v_pass EV_ADMIN.USUARIO.PASS%TYPE;

			v_Tipo EV_ADMIN.USUARIO.USUARIO%TYPE :='F';
		begin
		  open c_comprobarUsuario;
		  
		  fetch c_comprobarUsuario into v_usuario,v_pass;
		  while c_comprobarUsuario%found loop
			if ((v_usuario = p_usuario) and (v_pass = p_pass)) then
			  v_Tipo := 'Correcto';
			end if;  
		  fetch c_comprobarUsuario into v_usuario,v_pass;
		  end loop;
		  close c_comprobarUsuario;
		  if (v_Tipo = 'F') then 
			raise_application_error(-20302,'Password o usuario incorrecto');
		  else
			return v_Tipo;
		  end if;  
		end;
		/

	
